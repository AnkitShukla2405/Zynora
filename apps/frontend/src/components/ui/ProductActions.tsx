// --- Product Actions Component ---

interface Product {
    id: string
    name: string
    status: "active" | "draft" | "archived"
}

interface ProductActionsProps {
    product: Product
}

export function ProductActions({ product }: ProductActionsProps) {
    const [isOpen, setIsOpen] = React.useState(false)

    // Handlers would normally dispatch actions or call API
    const handleAction = (action: string) => {
        console.log(`Triggered action: ${action} for product ${product.id}`)
        setIsOpen(false)
    }

    const isPublished = product.status === "active"

    return (
        <DropdownMenu open={isOpen} onOpenChange={setIsOpen}>
            <DropdownMenuTrigger asChild>
                <Button
                    variant="ghost"
                    className="h-8 w-8 p-0 hover:bg-slate-100 data-[state=open]:bg-slate-100 dark:hover:bg-slate-800 dark:data-[state=open]:bg-slate-800"
                >
                    <span className="sr-only">Open menu</span>
                    <MoreHorizontal className="h-4 w-4 text-slate-500 hover:text-slate-900 dark:text-slate-400 dark:hover:text-slate-50" />
                </Button>
            </DropdownMenuTrigger>
            <DropdownMenuContent align="end" className="w-[160px]">
                <DropdownMenuLabel>Actions</DropdownMenuLabel>
                <DropdownMenuSeparator />

                <DropdownMenuItem onClick={() => handleAction("view")}>
                    View product
                </DropdownMenuItem>

                <DropdownMenuItem onClick={() => handleAction("edit")}>
                    Edit product
                </DropdownMenuItem>

                <DropdownMenuItem onClick={() => handleAction(isPublished ? "unpublish" : "publish")}>
                    {isPublished ? "Unpublish" : "Publish"}
                </DropdownMenuItem>

                <DropdownMenuItem onClick={() => handleAction("duplicate")}>
                    Duplicate product
                </DropdownMenuItem>

                <DropdownMenuSeparator />

                <DropdownMenuItem
                    onClick={() => handleAction("archive")}
                    className="text-red-600 focus:bg-red-50 focus:text-red-700 dark:text-red-400 dark:focus:bg-red-950/20 dark:focus:text-red-400"
                >
                    Archive product
                </DropdownMenuItem>
            </DropdownMenuContent>
        </DropdownMenu>
    )
}
